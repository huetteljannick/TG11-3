package a8Schleifen;

public class ForLoop {

    public static void main(String[] args){
        for (int zaehler = 0; zaehler < 5; zaehler++) {
            System.out.println(zaehler);
        }

        for (int zaehler = 5; zaehler > 0; zaehler--) {
            System.out.println(zaehler);
        }
    }
}